//解答：利用队列实现。push就还是队列的Push。pop就辅助再利用一个队列，弹出所有元素除了最后一个元素都加入另一个队列，再重新加回原来的队列
#include <queue>
using namespace std;
class MyStack {
public:
    queue<int> q;
    /** Initialize your data structure here. */
    MyStack() {
        
    }
    
    /** Push element x onto stack. */
    void push(int x) {
        q.push(x);
    }
    
    /** Removes the element on top of the stack and returns that element. */
    int pop() {
        int result;
        queue<int> temp;
        while(!q.empty())
        {
            result = q.front();
            q.pop();
            if(!q.empty())
                temp.push(result);
        }
        
        while(!temp.empty())
        {
            q.push(temp.front());
            temp.pop();
        }
        
        return result;
    }
    
    /** Get the top element. */
    int top() {
        return q.back();
    }
    
    /** Returns whether the stack is empty. */
    bool empty() {
        return q.empty();
    }
};

